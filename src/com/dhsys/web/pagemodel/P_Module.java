package com.dhsys.web.pagemodel;

public class P_Module implements java.io.Serializable{

	private Integer moduleId;

	private String moduleName;

	private String moduleUrl;

	private String moduleIcon;

	private String modulePath;

	private Integer pModuleId;

	public Integer getModuleId() {
		return moduleId;
	}

	public void setModuleId(Integer moduleId) {
		this.moduleId = moduleId;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getModuleUrl() {
		return moduleUrl;
	}

	public void setModuleUrl(String moduleUrl) {
		this.moduleUrl = moduleUrl;
	}

	public String getModuleIcon() {
		return moduleIcon;
	}

	public void setModuleIcon(String moduleIcon) {
		this.moduleIcon = moduleIcon;
	}

	public String getModulePath() {
		return modulePath;
	}

	public void setModulePath(String modulePath) {
		this.modulePath = modulePath;
	}

	public Integer getpModuleId() {
		return pModuleId;
	}

	public void setpModuleId(Integer pModuleId) {
		this.pModuleId = pModuleId;
	}
	
	
}
